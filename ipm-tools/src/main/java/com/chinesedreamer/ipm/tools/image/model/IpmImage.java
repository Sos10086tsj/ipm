package com.chinesedreamer.ipm.tools.image.model;

public class IpmImage {
	private String name;
	private String path;
	private String siez;
	public String getName() {
		return name;
	}
	public String getPath() {
		return path;
	}
	public String getSiez() {
		return siez;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public void setSiez(String siez) {
		this.siez = siez;
	}
	
	
}
